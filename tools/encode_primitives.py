#!/usr/bin/env python3
"""Encode retargeted G1 clips into the primitive library."""
import argparse
import json
import numpy as np


def encode_primitive(action, weapon, stance, source_id, features: np.ndarray, peak_idx: int, synthetic: bool = False):
    """Extract 4-frame peak window from features and emit a RON primitive snippet."""
    feature_dim = 414
    if features.shape[1] < feature_dim:
        # Pad stub features up to the schema dimension; real features must already be 414-dim.
        padded = np.zeros((features.shape[0], feature_dim), dtype=np.float32)
        padded[:, : features.shape[1]] = features
        features = padded
    assert features.shape[1] == feature_dim, features.shape
    window = features[peak_idx:peak_idx + 4]
    assert window.shape[0] == 4, window.shape
    root = window[-1, :3]
    heading = np.arctan2(window[-1, 5], window[-1, 4])
    marker = " // TEST_FIXTURE_DO_NOT_SHIP" if synthetic else ""
    ron = f"""({marker}
    action: {action},
    weapon: {weapon},
    stance: {stance},
    source_id: "{source_id}",
    feature_window: {json.dumps(window.tolist())},
    root_target: (position: [{root[0]:.6f}, {root[1]:.6f}, {root[2]:.6f}], heading: {heading:.6f}),
),"""
    return ron


def encode_library(primitives_ron: list[str]) -> str:
    body = "\n".join("        " + p.strip() for p in primitives_ron)
    return f"""// MotionBricks primitive library for Just Dodge.
// GENERATED BY tools/encode_primitives.py — DO NOT EDIT MANUALLY.
(
    version: "2026-07-09",
    skeleton: G1Skeleton34,
    feature_dim: 414,
    frames_per_token: 4,
    primitives: [
{body}
    ],
)
"""


def main():
    parser = argparse.ArgumentParser(description="Encode a retargeted clip into primitives.ron")
    parser.add_argument("--action", required=True)
    parser.add_argument("--weapon", required=True)
    parser.add_argument("--stance", required=True)
    parser.add_argument("--source", required=True)
    parser.add_argument("--features", required=True)
    parser.add_argument("--peak", type=int, required=True)
    parser.add_argument("--out", required=True)
    parser.add_argument("--synthetic", action="store_true", help="Mark the encoded primitive as a test fixture")
    args = parser.parse_args()

    features = np.load(args.features)
    primitive = encode_primitive(
        args.action,
        args.weapon,
        args.stance,
        args.source,
        features,
        args.peak,
        synthetic=args.synthetic,
    )
    library = encode_library([primitive])
    with open(args.out, "w") as f:
        f.write(library)
    print(library)


if __name__ == "__main__":
    main()
